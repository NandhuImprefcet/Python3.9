class cal1:
    def __init__(self):
        self.a=int(input())
        self.b=int(input())

    def sum(self):
        print(self.a+self.b)

    def sub(self):
        print(self.a-self.b)

    def mul(self):
        print(self.a*self.b)

    def div(self):
        print(self.a/self.b)

c1=cal1()
c1.sum()
c1.sub()
c1.mul()
c1.div()
